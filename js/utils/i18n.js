window.localizeLabel = function localizeLabel(swedishKey, language) {
  if (language !== "en") return swedishKey;
  return window.EN_LABELS[swedishKey] || swedishKey;
};

window.providerDisplayName = function providerDisplayName(provider, language) {
  if (provider === "gemini") return "Gemini";
  if (provider === "anthropic") return "Anthropic";
  if (provider === "openai") return "ChatGPT";
  if (provider === "grok") return "Grok";
  if (provider === "local") return language === "sv" ? "Lokal" : "Local";
  return provider;
};

